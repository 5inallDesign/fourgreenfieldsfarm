<p>The name of the farm comes from our pride in our Irish heritage and gratitude for the sacrifices our ancestors made to give us the chance to live in the greatest nation on earth. The term &quot;Four Green Fields&quot; refers to the 4 provinces of Ireland. It is also the title of a popular Irish folk song. You'll notice a shield on the farm sign inside the outline of Ireland. On it is the crest of each of the 4 provinces of Ireland. (Clockwise from the top left: Connaught, Ulster, Leinster and Munster). On the bottom of the sign are the locations &quot;Big Rapids Michigan, County Kerry, Ireland.&quot; That is because while we farm here, we are still in contact with the Courtneys &quot;on the land&quot; in County Kerry, Ireland, where my Great Grandfather was born. (We have a Rodney mailing address but are actually closer to Big Rapids.) Our farm motto that you see on our home page and on the bottom of the farm sign both reminds us of who we are at the core and of the sacrifices those that came before us made to give us the opportunities we enjoy today in this great Country. I almost forgot, the motto in Irish is pronounced &quot;O Erin go mer-i-caw, say-ree-i-meed on toliv&quot; and it means &quot;From Ireland to America We work the Land.&quot;</p>

