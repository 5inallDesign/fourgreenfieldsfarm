    <p>Four Green Fields Farm was established in 2003 and is comprised of 162 acres of diverse land including crop land, wooded area, wetlands and it partially borders a pond. We farm primarily hay to feed our two Belgian draft horse geldings - Rocky and Charlie - and to sell. We also plant a small amount of other crops such as corn, soybeans, wheat, and rye. The farm is home to a variety of wildlife including deer, turkeys, bobcats, coyotes, fox, Sandhill cranes, bald eagles, 4 varieties of turtles, at least 4 varieities of non-poisonous snakes (although we aren't real active in looking for them), squirrels, rabbits, grouse and assorted waterfowl. There has even been a sighting of a wandering black bear. Keep your eyes open as you never know what you might see.</p>
