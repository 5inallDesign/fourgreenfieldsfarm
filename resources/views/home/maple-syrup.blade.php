<h1>Making Maple Syrup</h1>

<p>We love the taste of real maple syrup here on the farm and so decided to make our own. We quickly realized that if we were going to put in the effort to make some for ourselves, we might as well make enough for others to enjoy. That is how we got into the syrup business. It doesn't hurt that making syrup is fun and a great way to get out in the woods as the seasons transition from winter to spring.</p>

<p>We started out producing our syrup with our good friend Andy Yoder, a local Amish farmer. Kevin and Andy get together on quite a few projects throughout the year and so getting together to make syrup was pretty easy. (It is interesting to note that Amish and non-Amish wives roll their eyes in the very same fashion at things their husbands say and do.) Andy's Dad Robert took over boiling the syrup for a couple of years and then decided to concentrate on his wood working business. We purchased his equipment in 2014 and will be sole operators in the spring of 2015. Fortunately, Andy and Robert are still our good friends and we see them often.</p>

<p>The process of making syrup is pretty simple. The sap from maple trees is collected and boiled down into syrup. Maple syrup is a truly wonderful delicious all natural product with no preservatives or additives. Besides using it on pancakes, waffles, and French toast, it can be used in baking, cooking, on top of ice cream, and as a sweetener in coffee and tea.</p>

<p>The first step in the process is to tap maple trees so that the sap from the trees can be gathered. We use the tried and true method of drilling a small hole into the tree and then inserting the &quot;tap&quot; which is a small hollow metal tube through which the sap from the tree will flow into a bucket which is hung on the tree. In large operations, plastic tubes can be attached to the taps and run to a large holding container or even pumped great distances to holding containers. Trees are tapped towards the end of winter when daytime temperatures begin to stay above freezing while night time temps stay below freezing. These conditions cause the sap to start to &quot;flow&quot;.</p>

