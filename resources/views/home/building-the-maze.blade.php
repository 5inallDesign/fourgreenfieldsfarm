<h1>Building the Maze</h1>

<p>Many of you might wonder how the Maze is created so here is everything you ever wanted to know about corn mazes but were afraid to ask.  The first step in making the maze is to draw it out.  Now you may have seen pictures of other Corn Mazes made in the shape of a pirate ship, rocket, President's face, or all sorts of things.  Well those are great but they are very expensive to have designed and so admission prices of 8 to 12 bucks are common at those mazes.</p>

<p>We like the idea of keeping our fun affordable ($3) so we go the &quot;do it yourself&quot; route.  Plus, every farmer in the world hates to pay for something they think they can do themselves.  We lay out the Maze using the dimensions of the field trying to make sure we keep enough space between paths.  We also try to design a pattern that presents as many multiple choice situations as possible.  Our attitude is the more times you have 3 choices of directions to go the more times you'll be wrong.</p>

<p>The next step is to plant a field of corn.  We do it a little different for a maze than we would for a non-maze field in that we plant rows both north and south and east and west.  That is to make the corn as thick as possible which makes a better maze. We also use limited herbicides and fertilizer along with rotating our crops to make sure the corn is nice and thick.  The result is the field is pretty environmentally friendly (full of weeds).</p>

<p>The actual making of the maze is done around the first part of July when the corn is just above knee height.  We layout the Maze using a 100ft tape and laser range finder.  Paths are marked using reflective poles made for marking driveway edges.  These poles are what the person mowing the paths with the tractor follows to cut the maze into the field.</p>

