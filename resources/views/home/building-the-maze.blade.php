<p>Once the Maze is laid out we use the farm's John Deere 850 tractor and brush hog pictured to the left to mow the pattern into the corn.  This picture shows one of the farm owners working on the south end of the 2008 maze.  His family thinks this photo shows his best side.</p>

<p>We then go over the paths with a rotto-tiller that fits on the same small tractor.  This smoothes the trails and chews up the corn stalks so they don't grow back.</p>

